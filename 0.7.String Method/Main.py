# capitalize
judul = "Kacang goreng yang di Panggang eNak sekali \n"
print('Capitalize')
print(judul.capitalize())

# title
print('Title')
print(judul.title())

# upper
print('upper')
print(judul.upper())

# lower
print('lower')
print(judul.lower())

# casefold sama seperti lower tapi ini lebih kuat mengubah karakter apapaun menjadi huruf kecil
print('casefold')
print(judul.casefold())

# swapcase sama seperti lower tapi ini lebih kuat mengubah karakter apapaun menjadi huruf kecil
print('swapcase')
print(judul.swapcase())